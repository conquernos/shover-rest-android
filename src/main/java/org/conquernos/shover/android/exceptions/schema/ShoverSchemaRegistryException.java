package org.conquernos.shover.android.exceptions.schema;


public class ShoverSchemaRegistryException extends ShoverSchemaException {

	public ShoverSchemaRegistryException() {
	}

	public ShoverSchemaRegistryException(String message) {
		super(message);
	}

	public ShoverSchemaRegistryException(Throwable cause) {
		super(cause);
	}

	public ShoverSchemaRegistryException(String message, Throwable cause) {
		super(message, cause);
	}

}
